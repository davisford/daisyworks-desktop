package com.daisyworks.service;

/**
 * 
 */
public class BluetoothServiceException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 */
	public BluetoothServiceException() {
		super();
	}

	/**
	 * @param arg0
	 */
	public BluetoothServiceException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public BluetoothServiceException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public BluetoothServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
